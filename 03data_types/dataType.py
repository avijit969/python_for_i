# numbers number, float, and complex
num1=10
print(type(num1))
num1=10.3
print(type(num1))
num1=10+2j
print(type(num1))

# Sequence Type - string tuple and list
# string
str1="isha"
print(type(str1))

# tuple
tup = (12,20,"avi","isha")
tup = 12,20,"avi","isha" # or
print(type(tup))

# list  
list1 = [12,20,"avi","isha",10.5,False,True]
print(type(list1))

# Boolean
isLogin=False
isLogin=True
print(type(isLogin))

# set
set1={1,2,"isha",10.4,False,1}
print(type(set1))

# Dictionary
dict1={
    "name":"isha",
    "age":19,
    "isLogin":True
    }
